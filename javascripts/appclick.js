
            function app_click(type){
                if (type == "calc"){
                    document.getElementById("app_show").src = "eng_calc.html";
                }
                else if (type == "game"){
                    document.getElementById("app_show").src = "eng_game.html";
                }
                else if (type == "internet"){
                    document.getElementById("app_show").src = "eng_internet.html";
                }
                else if (type == "slide"){
                    document.getElementById("app_show").src = "eng_slide_show.html";
                }
                else if (type == "youtube"){
                    document.getElementById("app_show").src = "eng_youtube.html";
                }
                else if (type == "change"){
                    document.getElementById("app_show").src = "eng_changelog.html";
                }
                else if (type == "about"){
                    document.getElementById("app_show").src = "eng_about.html";
                }
                else if (type == "file"){
                    document.getElementById("app_show").src = "eng_win_file.html";
                }
                else {
                    //////noooo
                }
            }
        